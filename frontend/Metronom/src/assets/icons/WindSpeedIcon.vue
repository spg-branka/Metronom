<script setup>
import { computed, defineProps } from 'vue'

const props = defineProps({
	value: String,
})

const windSpeed = computed(() => {
	const match = props.value.match(/(\d+\.?\d*)/)
	return match ? parseFloat(match[1]) : 0
})

const animationDuration = computed(() => {
	if (windSpeed.value <= 0) return '0s'

	const minDuration = 0.3
	const maxDuration = 10

	const duration = maxDuration - ((windSpeed.value / 100) * (maxDuration - minDuration))

	return `${Math.max(minDuration, duration).toFixed(2)}s`
})

const animationDurationLogarithmic = computed(() => {
	if (windSpeed.value <= 0) return '0s'

	const minDuration = 0.3
	const maxDuration = 15

	const logSpeed = Math.log10(windSpeed.value + 1)
	const logMax = Math.log10(101)

	const duration = maxDuration - ((logSpeed / logMax) * (maxDuration - minDuration))

	return `${Math.max(minDuration, duration).toFixed(2)}s`
})
